/**
 * Exercicio19_TreinamentoDeCS
 *
 * Simule o abate de 10 minions com um for loop.
 * A cada iteração, imprima "Minion abatido!".
 */

public class Exercicio19_MinionSlayer {
    public static void main(String args[]) {
        for (int i = 1; i <= 10; i++) {
            System.out.println("[" + i + "] Minions abatidos!");
        }
    }
}